// Using itreation print Jay Ganesh 5 times on screen 

class Arithematic
{
    void Display()
    {
        //itreation 
        int icnt = 0;
        for(icnt = 1; icnt <= 5; icnt++) 
        {
            System.out.println("Jay Ganesh ...");
        }
    }
}

public class program12
{
    public static void main(String a[])
    {

        Arithematic aobj = new Arithematic();

        aobj.Display();
    }
}

